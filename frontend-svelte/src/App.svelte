  import Sensitivity from "./lib/Sensitivity.svelte";
  import Result from "./lib/Result.svelte";
  import ImageSelect from "./lib/ImageSelect.svelte";

  let image: string; // data:image/jpeg;base64
  let snackMsg: string;
  let snackbarError: SnackbarComponentDev;
  let imageDatas: ImageData[];
  let loading = false;

  export let server: string;
  let sensitivity: number = 45;

  const send = () => {
    if (image === undefined || image === "") {
      snackMsg = "Du m친 velge et bilde..";
      snackbarError.open();
      return;
    }

    const body = {
      sensitivity: sensitivity,
      image: image,
    };
    loading = true;

    const sendResponse = fetch(server, {
      method: "post",
      body: JSON.stringify(body),
      headers: {
        "Content-Type": "application/json",
      },
    });

    sendResponse
      .then((res) => res.json())
      .then((data: ImageData[]) => (imageDatas = data))
      .catch((e) => {
        console.error(e);
        snackMsg = e.toString();
        snackbarError.open();
        imageDatas = [];
      })
      .finally(() => (loading = false));
  };
</script>

